    private void listValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listValueChanged
        DefaultListModel listModel = new DefaultListModel();
        ContactInfo contactInfo = dbConnection.findByName(list.getSelectedValue());

        nameField.setText(contactInfo.getName());
        phoneField.setText(contactInfo.getPhone());
        emailField.setText(contactInfo.getEmail());
        addressField.setText(contactInfo.getAddress());
        this.setImageTitle(contactInfo.getImageTitle());

//      Setting image to contactImageLabel
        ImageIcon contactImage = new ImageIcon(new ImageIcon("images\\" + this.getImageTitle()).getImage().getScaledInstance(contactImageLabel.getWidth(), contactImageLabel.getHeight(), Image.SCALE_DEFAULT));
        contactImageLabel.setIcon(contactImage);

    }//GEN-LAST:event_listValueChanged

//  Searching contactInfo when typing into search textfield
    private void searchFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchFieldKeyReleased
        DefaultListModel listModel = new DefaultListModel();
        List<String> nameList = dbConnection.search(searchField.getText());
        for (String name : nameList) {
            listModel.addElement(name);
        }
        this.getList().setModel(listModel);
        this.getList().setSelectedIndex(0);
    }//GEN-LAST:event_searchFieldKeyReleased

//  Used for deleting existing contactInfo as well ass image associated with that contact.
    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (!list.isSelectionEmpty()) {
            int option = JOptionPane.showConfirmDialog(this, "Do you really want to delete?", "Delete!!!", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                new File("images\\" + imageTitle).delete();
                dbConnection.delete(phoneField.getText());
                refreshList();
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

//  Opens Edit Jframe window.
    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        if (!list.isSelectionEmpty()) {
            Edit editWindow = new Edit();
            editWindow.getNameField().setText(nameField.getText());
            editWindow.getPhoneField().setText(phoneField.getText());
            editWindow.getEmailField().setText(emailField.getText());
            editWindow.getAddressField().setText(addressField.getText());
            editWindow.setOldPhoneNumber(phoneField.getText());
            editWindow.setImageTitle(imageTitle);
            ImageIcon contactImage = new ImageIcon(new ImageIcon("images\\" + imageTitle).getImage().getScaledInstance(editWindow.getContactImageLabel().getWidth(), editWindow.getContactImageLabel().getHeight(), Image.SCALE_DEFAULT));
            editWindow.getContactImageLabel().setIcon(contactImage);
            
            editWindow.setMainWindow(this);
            editWindow.setLocationRelativeTo(null);
            editWindow.setVisible(true);
        }
    }//GEN-LAST:event_editButtonActionPerformed

