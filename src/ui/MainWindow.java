//  This method is used for refreshing the Jlist of contact when new contact is added or existing contact is deleted.
    public void refreshList() {
        DefaultListModel listModel = new DefaultListModel();
        List<String> names = dbConnection.findAllName();
        for (String name : names) {
            listModel.addElement(name);
        }
        this.getList().setModel(listModel);
        this.getList().setSelectedIndex(0);
    }

